                            <h3>Why We Need Your Support For Disability Program</h3>
                            <span class="heading-border"></span>
                            <p>In India and many other countries, the disabled person is often looked upon as a burden on the society and are unable to become productive member of the society.In the past, the disability related issues have been ignored widely both by the government and the NGO. <br><br>Currently they are being recognized as a cross cutting development issue and also are gradually being seen more as an issue of right than Charity. In order to enhance socio-economic development, the country cannot afford to leave these segments of people out of the development process planned.<br><br> Development is neither possible nor desirable without comprehensive and sensible program for all section of people.</p>
                        </div>
						
						
						
						<h3 class="mt-30">To Make A Long-Term Impact</h3>
						<ul class="check-list">
						<li><i class="fa fa-check"></i>To establish, run and implement a community-based sustainable disabled friendly program in order to provide support to empower the people with disability in attaining rights, opportunities, livelihood, easy movement, access to everywhere, social reputation and economic self-sufficiency.</li>
<li><i class="fa fa-check"></i>Developing a free app (Android and ios) to help people with disabilities and limited mobility to access venues, public places, restaurants and entertainment hotspots in major cities in India to begin with.</li>
<li><i class="fa fa-check"></i>To create an atmosphere that is stimulating, encouraging, safe, clean, healthy and adaptive to each disabled child’s individual needs; to empower and encourage the persons with Different Types of Disabilities to develop to their full potential by improving their physical or mental performance and thus enjoying a better quality of life.</li>
<li><i class="fa fa-check"></i>To assess the mental and physical health of each child, provide daily attention; to measure the abilities of children and put the activities that suit their levels, and to utilize their capabilities to the maximum limit possible.</li>
<li><i class="fa fa-check"></i>To take care of persons with disabilities on daily basis and to assist them and to alleviate their families’ burden; enabling the parents, especially the mothers to be relieved of 24-hour care and help them go to work and take time for leisure.</li>
<li><i class="fa fa-check"></i>To develop skills of the disabled and helpless persons, so that as they grow, they can be self-reliant, skillful and capable in the family and community; to integrate children with special needs into adequate educational programs; wherever possible, integrate the child by admitting them into the formal schools in accordance to their skills and ability.</li>
<li><i class="fa fa-check"></i>The Safe hands rehabilitation society ‘Day Care Centre’ long-term objective is to provide these Rural and underprivileged persons with the opportunity to live independently in the future; to provide individualized care and counseling to the parents of the persons with disabilities.</li>
<li><i class="fa fa-check"></i>To train the mothers on the way to look after their disabled persons; to enable her to provide physiotherapy, speech therapy, functional therapy to rehabilitate the persons with disability.</li>
<li><i class="fa fa-check"></i>To organize the public awareness programs for changing the negative feeling of the society towards the disabled people</li>
